public class Operator {
    public static void main(String[] args) {
        int a = 12;
        int b = 7;
        int c = 15+9;
        System.out.println("c = " + c);
        System.out.println("a + b = " + (a + b));

        // logical operators
        boolean x = true;
        boolean y = false;
        System.out.println("x && y = " + (x && y));
        System.out.println("x || y = " + (x || y));

    }
}